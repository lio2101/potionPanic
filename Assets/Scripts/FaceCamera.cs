using UnityEngine;

namespace LJ
{
    public class FaceCamera : MonoBehaviour
    {
        // --- Enums ------------------------------------------------------------------------------------------------------

        // --- Fields -----------------------------------------------------------------------------------------------------
        private Camera _camera;

        // --- Properties -------------------------------------------------------------------------------------------------

        // --- Unity Functions --------------------------------------------------------------------------------------------
        private void Awake()
        {
            _camera = Camera.main;
        }

        private void LateUpdate()
        {
            if(_camera == null)
            {
                _camera = Camera.main;
            }

            transform.forward = _camera.transform.forward;
        }

        // --- Event callbacks --------------------------------------------------------------------------------------------

        // --- Public/Internal Methods ------------------------------------------------------------------------------------

        // --- Protected/Private Methods ----------------------------------------------------------------------------------

        // --------------------------------------------------------------------------------------------
    }
}